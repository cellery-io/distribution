package io.cellery.cell.api.publisher.beans.controller;

import com.fasterxml.jackson.annotation.JsonProperty;
import io.cellery.cell.api.publisher.utils.Constants;

/** Class to represent API destination. */
public class ApiDestination {

  @JsonProperty(Constants.JsonParamNames.HOST)
  private String host;

  @JsonProperty(Constants.JsonParamNames.PORT)
  private String port;

  public String getHost() {
    return host;
  }

  public void setHost(String host) {
    this.host = host;
  }

  public String getPort() {
    return port;
  }

  public void setPort(String port) {
    this.port = port;
  }
}
